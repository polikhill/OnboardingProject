import UIKit

extension UIColor {
  @nonobjc class var freshGreen: UIColor {
    return UIColor(red: 104.0 / 255.0, green: 212.0 / 255.0, blue: 73.0 / 255.0, alpha: 0.5)
  }
  
  @nonobjc class var lightishRed: UIColor {
    return UIColor(red: 1.0, green: 47.0 / 255.0, blue: 74.0 / 255.0, alpha: 0.5)
  }
}
